    private void initClaim() {
        if (_userSession.getClaim() == Constants.CLAIM_MANAGER) {
            btnStaff.doClick();
            btnTrainees.setEnabled(false);
            btnDistribution.setEnabled(false);
            btnStatistics.setEnabled(false);
        } else if (_userSession.getClaim() == Constants.CLAIM_ACCOUNTANT) {
            btnDistribution.doClick();
            btnCourse.setEnabled(false);
            btnClass.setEnabled(false);
            btnStaff.setEnabled(false);
        }
        else
            btnDistribution.doClick();
    }
